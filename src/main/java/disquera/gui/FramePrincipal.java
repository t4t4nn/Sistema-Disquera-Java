    public FramePrincipal() 
    {
        initComponents();
        this.setResizable(false);
        this.setLocationRelativeTo(null); 
        String rutaImagen = "/disquera/imagenes/foto.png";
        java.net.URL url = getClass().getResource(rutaImagen);
        
        if (url != null) 
        {
            try 
            {
               
                javax.swing.ImageIcon imagenOriginal = new javax.swing.ImageIcon(url);
                
                
                int anchoVentana = this.getWidth();
                int altoVentana = this.getHeight();
                
               
                java.awt.Image imagenEscalada = imagenOriginal.getImage().getScaledInstance(anchoVentana, altoVentana, java.awt.Image.SCALE_SMOOTH);
                
                
                lblFondo.setIcon(new javax.swing.ImageIcon(imagenEscalada));
                lblFondo.setSize(anchoVentana, altoVentana);
                lblFondo.setLocation(0, 0);
                
            } catch (Exception e) 
            {
                System.out.println("Error al ajustar imagen: " + e.getMessage());
            }
        }
        else 
        {
            System.out.println("No se encontró la imagen de fondo.");
        }
        
    }
    
