    private void ajustarImagenAlTamaño() {
        if (imagenFondoOriginal != null) {
            
            int anchoVentana = this.getWidth();
            int altoVentana = this.getHeight();
            
            
            if (anchoVentana > 0 && altoVentana > 0) {
                
                
                java.awt.Image imagenEscalada = imagenFondoOriginal.getImage().getScaledInstance(anchoVentana, altoVentana, java.awt.Image.SCALE_SMOOTH);
                
                
                lblFondo.setIcon(new javax.swing.ImageIcon(imagenEscalada));
                
                
                lblFondo.setSize(anchoVentana, altoVentana);
                lblFondo.setLocation(0, 0);
            }
        }
    }
     
    
